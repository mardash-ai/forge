export interface EnvProdExampleOptions {
  appName: string;
  host: string;
  withPostgres: boolean;
  withRedis: boolean;
  secrets: string[];
  // The app declares scheduled jobs (forge.jobs.json) — the compose wires them
  // automatically, so the example documents that instead of the optional seam.
  withJobs?: boolean;
}

// The `#` comment block that precedes each secret in .env.prod.example — what it is,
// which capability needs it, required-vs-optional, how to obtain, how to generate.
// Catalog-driven (C13) so the operator never sees a bare, unexplained `NAME=`.
function envCommentLines(spec: SecretSpec): string[] {
  const lines = [
    `# ${spec.name} — ${spec.what}`,
    `#   ${spec.capability} · ${requirementLabel(spec)}. ${spec.requires_note}`,
    `#   Obtain: ${spec.obtain}`,
  ];
  if (spec.generate) lines.push(`#   Generate: ${spec.generate}`);
  return lines;
}

export function generateEnvProdExample(opts: EnvProdExampleOptions): string {
  const lines: string[] = [
    `# Generated by Forge — Productionize. Production config for ${opts.appName}.`,
    `# Copy to .env.prod, fill in real values, and DO NOT commit .env.prod.`,
    `# Consumed at deploy time (e.g. \`docker compose -f compose.prod.yaml --env-file .env.prod ...\`).`,
    `# Public host (baked into compose.prod.yaml Traefik labels): ${opts.host}`,
    `# Full operator guide + how to obtain each value: ${PROVISIONING_FILE} (this app) and forge/PROVISIONING.md.`,
    '',
    '# --- Forge secrets vault master key (C5) ---',
    ...envCommentLines(describeSecret('FORGE_SECRETS_KEY')),
    'FORGE_SECRETS_KEY=change-me',
    '',
  ];
  if (opts.withPostgres) {
    lines.push('# --- Postgres (required when the app is provisioned with a database) ---');
    lines.push(...envCommentLines(describeSecret('POSTGRES_PASSWORD')));
    lines.push('POSTGRES_PASSWORD=change-me');
    lines.push('');
  }
  if (opts.secrets.length) {
    lines.push('# --- Declared secrets (injected into the web AND data-plane containers; never commit real values) ---');
    for (const s of opts.secrets) {
      lines.push(...envCommentLines(describeSecret(s)));
      lines.push(`${s}=`);
      lines.push('');
    }
  }
  if (opts.withJobs) {
    lines.push('# --- Scheduled jobs (C2) ---');
    lines.push(`# ${JOBS_FILE} is mounted into the data-plane sidecar and registered on boot — no env needed.`);
  } else {
    lines.push('# --- Optional: scheduled jobs (C2) the data-plane sidecar registers on boot ---');
    lines.push(`# Declare jobs in ${JOBS_FILE} at the repo root (re-run \`forge productionize\` to wire it), or:`);
    lines.push(`# FORGE_JOBS_FILE=/app/${JOBS_FILE}`);
  }
  return lines.join('\n') + '\n';
}

