// A container healthcheck (node fetch) — the roll (C7) gates on this container
// health before draining the old replica. Mirrors the runtime-docker-compose form.
function healthTest(url: string): string {
  return `["CMD", "node", "-e", "fetch('${url}').then(r=>process.exit(r.ok?0:1)).catch(()=>process.exit(1))"]`;
}

export function generateProdCompose(opts: ProdComposeOptions): string {
  const {
    appName,
    port,
    host,
    readinessPath,
    webImage,
    dataPlaneImage,
    withPostgres,
    withRedis,
    secrets,
    certResolver,
  } = opts;
  const withJobs = opts.withJobs ?? false;
  const withTheme = opts.withTheme ?? false;
  const platformDb = opts.platformDb ?? false;
  // A postgres service exists if the app has its OWN database OR platform storage is on Postgres.
  const hasPostgres = withPostgres || platformDb;
  const proxyNet = opts.proxyNet ?? 'proxy';
  const db = dbNameFor(appName);
  const blobsBackend = opts.blobsBackend ?? 'filesystem';
  const observability = opts.observability ?? false;
  // C36 — the OTLP→Langfuse export env for a tier. The endpoint defaults to the internal Langfuse
  // address; the LANGFUSE keys are empty-default (`${VAR:-}`) so a missing key silently DISABLES tracing
  // (the tier is unaffected) instead of failing the deploy — observability must never take the app down.
  const otelEnv = (serviceName: string): string[] =>
    observability
      ? [
          '      - OTEL_EXPORTER_OTLP_ENDPOINT=${OTEL_EXPORTER_OTLP_ENDPOINT:-http://langfuse-web:3000/api/public/otel}',
          `      - OTEL_SERVICE_NAME=${serviceName}`,
          '      - LANGFUSE_PUBLIC_KEY=${LANGFUSE_PUBLIC_KEY:-}',
          '      - LANGFUSE_SECRET_KEY=${LANGFUSE_SECRET_KEY:-}',
        ]
      : [];
  // Joins the web + sidecar to the shared external `observability` network (where the Langfuse stack
  // lives) + declares it at the bottom — only when observability is on.
  const observabilityNet = observability ? '\n      - observability' : '';
  const observabilityNetDecl = observability ? '\n  observability:\n    external: true' : '';
  // Traefik router/service keys must be a safe slug.
  const svc = appName.replace(/[^a-zA-Z0-9_-]/g, '-');

  // P34/P36 — the secret env lines shared by BOTH tiers. Each declared secret interpolates from .env.prod
  // (never a value in this file). P36: AUTH_SERVICE_TOKEN is forced DEPLOY-REQUIRED (`${VAR:?…}`) when the
  // app declares scheduled cron jobs, so an unset token fails the deploy loudly instead of firing bare,
  // unauthenticated POSTs at publicly-routed `/api/cron/*`.
  const usesAuth = secrets.includes(AUTH_SESSION_SECRET_VAR);
  const cronTokenInterp = `\${${AUTH_SERVICE_TOKEN_VAR}:?${CRON_TOKEN_REQUIRED_REASON}}`;
  const interpFor = (name: string): string =>
    withJobs && name === AUTH_SERVICE_TOKEN_VAR ? cronTokenInterp : secretInterpolation(name);
  const sharedSecretEnv: string[] = secrets.map((s) => `      - ${s}=${interpFor(s)}`);
  if (withJobs && !secrets.includes(AUTH_SERVICE_TOKEN_VAR)) {
    sharedSecretEnv.push(`      - ${AUTH_SERVICE_TOKEN_VAR}=${cronTokenInterp}`);
  }
  // P34 — the OPTIONAL auth-provider vars the data-plane reads (it hosts C10 /auth/*), wired ONLY into the
  // sidecar as defined-but-empty when the app uses hosted auth and hasn't already declared them, so an
  // operator enables Google/SMTP by filling .env.prod alone.
  const dpAuthProviderEnv: string[] = usesAuth
    ? AUTH_PROVIDER_VARS.filter((v) => !secrets.includes(v)).map((v) => `      - ${v}=\${${v}:-}`)
    : [];

  const dependsOn: string[] = [];
  if (hasPostgres) dependsOn.push('      postgres:\n        condition: service_healthy');
  dependsOn.push('      data-plane:\n        condition: service_started');

  // Web environment: prod NODE_ENV, the data-plane sidecar base URL, DB/Redis URLs
  // from infra, and one interpolation line per declared secret (`${NAME:-}` keeps it
  // defined-but-empty when unset, so the app degrades instead of crashing — EXCEPT a
  // deploy-required secret like AUTH_SESSION_SECRET, which the catalog emits as
  // `${NAME:?…}` so a missing/empty session-signing key FAILS the deploy loudly instead
  // of silently logging every user out. Real values come from .env.prod, never this
  // file). The data-plane base is published
  // under BOTH names: FORGE_EVENTS_URL is the contract the shipped C1/C3/C4 app
  // clients actually read; FORGE_DATA_PLANE_URL is kept as a compatible alias.
  const webEnv: string[] = [
    '      - NODE_ENV=production',
    `      - PORT=${port}`,
    '      - HOSTNAME=0.0.0.0',
    `      - FORGE_EVENTS_URL=http://data-plane:${DATA_PLANE_PORT}`,
    `      - FORGE_DATA_PLANE_URL=http://data-plane:${DATA_PLANE_PORT}`,
  ];
  if (withPostgres) {
    webEnv.push(
      `      - DATABASE_URL=postgres://forge:\${POSTGRES_PASSWORD:?set POSTGRES_PASSWORD in .env.prod}@postgres:5432/${db}`,
    );
  }
  if (withRedis) webEnv.push('      - REDIS_URL=redis://redis:6379');
  webEnv.push(...sharedSecretEnv);
  // C36 — the app tier emits the `app.mcp.dispatch` span (continues the transport trace).
  webEnv.push(...otelEnv(appName));

  // Traefik ingress: host rule + the loadbalancer healthcheck the roll relies on.
  const labels: string[] = [
    '      - "traefik.enable=true"',
    `      - "traefik.docker.network=${proxyNet}"`,
    `      - "traefik.http.routers.${svc}.rule=Host(\`${host}\`)"`,
    `      - "traefik.http.routers.${svc}.entrypoints=websecure"`,
    `      - "traefik.http.routers.${svc}.tls=true"`,
    `      - "traefik.http.routers.${svc}.tls.certresolver=${certResolver}"`,
    `      - "traefik.http.services.${svc}.loadbalancer.server.port=${port}"`,
    `      - "traefik.http.services.${svc}.loadbalancer.healthcheck.path=${readinessPath}"`,
    `      - "traefik.http.services.${svc}.loadbalancer.healthcheck.interval=10s"`,
  ];

  const web = `  web:
    image: ${webImage}
    restart: unless-stopped
    environment:
${webEnv.join('\n')}
    depends_on:
${dependsOn.join('\n')}
    networks:
      - ${proxyNet}
      - internal${observabilityNet}
    stop_grace_period: 30s
    healthcheck:
      test: ${healthTest(`http://localhost:${port}${readinessPath}`)}
      interval: 10s
      timeout: 5s
      retries: 5
      start_period: 20s
    labels:
${labels.join('\n')}`;

  // The Forge data-plane sidecar (C3 app-event log + C4 notifications + C2
  // scheduler + C1 agent-run + C5 secrets read). The web app reaches it at
  // http://data-plane:3718 (FORGE_EVENTS_URL). Its store persists on a volume.
  //
  // Its DATA-plane capabilities run here, so it needs the same runtime injection the
  // web tier gets (P6): FORGE_SECRETS_KEY to DECRYPT the C5 vault at rest (on the
  // forge_state volume) + the declared secrets on the env (the process-env fallback
  // C1 agent-run reads when the vault is empty). Real values come from .env.prod.
  const dpEnv: string[] = [
    `      - PORT=${DATA_PLANE_PORT}`,
    `      - FORGE_APP_NAME=${appName}`,
    `      - FORGE_APP_REPO_PATH=${APP_REPO_MOUNT}`,
    '      - FORGE_STATE_DIR=/forge-state',
    // C5 vault master key — without it the sidecar can't decrypt secrets (C1 → 503).
    '      - FORGE_SECRETS_KEY=${FORGE_SECRETS_KEY:-}',
    // How the sidecar reaches the web tier over the internal network — the C2 scheduler
    // callback AND the C15 status page's live C6 health probe use these.
    '      - FORGE_APP_CALLBACK_HOST=web',
    `      - FORGE_APP_CALLBACK_PORT=${port}`,
    `      - FORGE_READINESS_PATH=${readinessPath}`,
  ];
  dpEnv.push(...sharedSecretEnv);
  // P34 — the optional Google/SMTP provider vars the sidecar reads for hosted auth (defined-but-empty
  // unless the operator fills .env.prod). Data-plane only — the web tier proxies /auth/* here.
  dpEnv.push(...dpAuthProviderEnv);
  // P38 — SPLIT-HOST auth public URL. The data-plane hosts C10 `/auth/*`, but it builds every auth URL
  // (the OAuth redirect_uri, the verify/reset links, and the post-login redirect) from `publicBase`, which
  // defaults to the host the request ARRIVED on. When the UI lives on a DIFFERENT public host than the API
  // (e.g. the web tier on app.<domain> proxying `/auth/*` to the platform on api.<domain>), that arrival
  // host is the API host — so a Google login callback + its host-only session cookie + the `/home` bounce
  // all land on the API host, stranding the user off the app. FORGE_AUTH_PUBLIC_URL pins the USER-FACING
  // origin (publicBase reads it FIRST); the data-plane only reads it, so we wire it defined-but-empty from
  // .env.prod for hosted-auth apps. Empty = today's request-host-derived behavior, so single-host apps are
  // unaffected. (Not on the web tier — it proxies `/auth/*` here and never computes an auth URL itself.)
  if (usesAuth) dpEnv.push('      - FORGE_AUTH_PUBLIC_URL=${FORGE_AUTH_PUBLIC_URL:-}');
  // P38 companion for the C24 CONNECTORS flow (`/connect/*`): its publicBase reads a SEPARATE var,
  // FORGE_OAUTH_PUBLIC_URL. Same split-host rationale — a "Connect Google" callback + its post-connect
  // `return_to` bounce (a relative path like /settings/integrations) must land on the USER-FACING host,
  // not the API host the proxied request arrived on. Wired defined-but-empty from .env.prod; empty =
  // request-host-derived (single-host apps unaffected). Data-plane only (the web tier proxies /connect/*).
  if (usesAuth) dpEnv.push('      - FORGE_OAUTH_PUBLIC_URL=${FORGE_OAUTH_PUBLIC_URL:-}');
  // C36 — the transport tier emits the `mcp.tool_call` trace ROOT (initOtelLangfuse reads these at boot).
  dpEnv.push(...otelEnv('forge-data-plane'));
  // The data-plane's ENTIRE state dir (FORGE_STATE_DIR=/forge-state) rides ONE durable
  // named volume. This is load-bearing for auth: C10's users/sessions/`forge_refresh`
  // records live at /forge-state/auth/<appId>.json, so the volume is what lets a signed-in
  // user SURVIVE a deploy — `forge deploy` recreates this sidecar onto the new pin, and a
  // named volume persists across that recreate (an ephemeral fs would wipe every session +
  // refresh token → 401 on /auth/session AND /auth/refresh → every user logged out). The
  // C5 secrets vault (/forge-state/secrets) rides the same volume, so its master key never
  // regenerates across a recreate either. Do NOT drop forge_state.
  const dpVolumes: string[] = ['      - forge_state:/forge-state'];
  if (withJobs) {
    // The app declares scheduled jobs — mount the declaration read-only and pin
    // FORGE_JOBS_FILE at it so C2 registers them on boot (P7.3). The registered
    // ScheduledJob state then persists on the forge_state volume.
    dpEnv.push(`      - FORGE_JOBS_FILE=${APP_REPO_MOUNT}/${JOBS_FILE}`);
    dpVolumes.push(`      - ./${JOBS_FILE}:${APP_REPO_MOUNT}/${JOBS_FILE}:ro`);
  } else {
    // No declared jobs — leave the seam optional so an operator can still point at one.
    dpEnv.push('      - FORGE_JOBS_FILE=${FORGE_JOBS_FILE:-}');
  }
  if (withTheme) {
    // The app declares a theme (C16) — mount it read-only and pin FORGE_THEME_FILE so
    // the hosted auth + status pages render branded in production.
    dpEnv.push(`      - FORGE_THEME_FILE=${APP_REPO_MOUNT}/${THEME_FILE}`);
    dpVolumes.push(`      - ./${THEME_FILE}:${APP_REPO_MOUNT}/${THEME_FILE}:ro`);
  } else {
    // No declared theme — the seam stays optional; pages fall back to the neutral default.
    dpEnv.push('      - FORGE_THEME_FILE=${FORGE_THEME_FILE:-}');
  }

  // P26 — platform storage on Postgres: select the backend + point the sidecar at the SEPARATE
  // forge_platform database, and make it wait for Postgres to be healthy (the sidecar fail-fasts on
  // an unreachable/misconfigured datastore at boot). Absent = the filesystem default (unchanged).
  const dpDependsOn: string[] = [];
  if (platformDb) {
    dpEnv.push('      - FORGE_STORE_BACKEND=postgres');
    dpEnv.push(`      - FORGE_DB_URL=${platformDbUrl()}`);
    dpDependsOn.push('      postgres:\n        condition: service_healthy');
  }
  // P33 — blobs (C20) ride the filesystem (forge_state volume) by DEFAULT, DECOUPLED from the structured
  // store above: a Postgres platform store no longer drags blobs onto S3. Only an explicit `blobsBackend:
  // 's3'` flips the sidecar to the object store (operator supplies FORGE_S3_* in .env.prod).
  if (blobsBackend === 's3') {
    dpEnv.push('      - FORGE_BLOBS_BACKEND=s3');
    for (const v of ['FORGE_S3_ENDPOINT', 'FORGE_S3_BUCKET', 'FORGE_S3_ACCESS_KEY', 'FORGE_S3_SECRET_KEY', 'FORGE_S3_REGION']) {
      dpEnv.push(`      - ${v}=\${${v}:-}`);
    }
  }
  const dpDependsBlock = dpDependsOn.length ? `\n    depends_on:\n${dpDependsOn.join('\n')}` : '';

  const dataPlane = `  data-plane:
    image: ${dataPlaneImage}
    restart: unless-stopped${dpDependsBlock}
    environment:
${dpEnv.join('\n')}
    volumes:
${dpVolumes.join('\n')}
    networks:
      - internal${observabilityNet}
    stop_grace_period: 15s
    healthcheck:
      test: ${healthTest(`http://localhost:${DATA_PLANE_PORT}/health`)}
      interval: 10s
      timeout: 5s
      retries: 5
      start_period: 10s`;

  const services: string[] = [web, dataPlane];
  const volumes: string[] = ['  forge_state:'];

  if (hasPostgres) {
    if (withPostgres) {
      // The app has its OWN database (role `forge`, db `${db}`). When platform storage ALSO rides
      // Postgres, inject the platform password + mount the first-init script that creates the SEPARATE
      // least-privilege forge_platform role + database on the same instance (isolation + least priv).
      const pgEnv = [
        '      - POSTGRES_USER=forge',
        '      - POSTGRES_PASSWORD=${POSTGRES_PASSWORD:?set POSTGRES_PASSWORD in .env.prod}',
        `      - POSTGRES_DB=${db}`,
      ];
      const pgVolumes = ['      - postgres_data:/var/lib/postgresql/data'];
      if (platformDb) {
        pgEnv.push('      - FORGE_PLATFORM_DB_PASSWORD=${FORGE_PLATFORM_DB_PASSWORD:?set FORGE_PLATFORM_DB_PASSWORD in .env.prod}');
        pgVolumes.push(`      - ./${PLATFORM_DB_INIT_FILE}:/docker-entrypoint-initdb.d/${PLATFORM_DB_INIT_FILE}:ro`);
      }
      services.push(`  postgres:
    image: postgres:16-alpine
    restart: unless-stopped
    environment:
${pgEnv.join('\n')}
    volumes:
${pgVolumes.join('\n')}
    networks:
      - internal
    stop_grace_period: 30s
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U forge -d ${db}"]
      interval: 10s
      timeout: 5s
      retries: 5`);
    } else {
      // App has NO database of its own — a DEDICATED Postgres for platform state, initialized directly
      // as the forge_platform role/db by the image entrypoint (no init script needed).
      services.push(`  postgres:
    image: postgres:16-alpine
    restart: unless-stopped
    environment:
      - POSTGRES_USER=${PLATFORM_DB_ROLE}
      - POSTGRES_PASSWORD=\${FORGE_PLATFORM_DB_PASSWORD:?set FORGE_PLATFORM_DB_PASSWORD in .env.prod}
      - POSTGRES_DB=${PLATFORM_DB_NAME}
    volumes:
      - postgres_data:/var/lib/postgresql/data
    networks:
      - internal
    stop_grace_period: 30s
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U ${PLATFORM_DB_ROLE} -d ${PLATFORM_DB_NAME}"]
      interval: 10s
      timeout: 5s
      retries: 5`);
    }
    volumes.push('  postgres_data:');
  }

  if (withRedis) {
    services.push(`  redis:
    image: redis:7-alpine
    restart: unless-stopped
    networks:
      - internal
    stop_grace_period: 10s
    healthcheck:
      test: ["CMD", "redis-cli", "ping"]
      interval: 10s
      timeout: 5s
      retries: 5`);
  }

  return `# Generated by Forge — Productionize (productionize-nextjs-compose).
# The CANONICAL production stack — exactly what \`forge deploy\` rolls (start-first,
# behind Traefik on the "${proxyNet}" network). Do not hand-edit service topology;
# re-run \`forge productionize\` instead. Secrets/passwords come from .env.prod
# (see .env.prod.example) and are NEVER committed here. Images are digest-pinned (R1).
name: forge-${appName}-prod

networks:
  ${proxyNet}:
    external: true
  internal:
    driver: bridge${observabilityNetDecl}

services:
${services.join('\n\n')}

volumes:
${volumes.join('\n')}
`;
}

