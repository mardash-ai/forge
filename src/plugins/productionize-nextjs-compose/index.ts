// The generation Implementation behind the Productionize Capability. It emits the
// app's CANONICAL production artifacts — the ones `forge deploy` (C7) rolls:
//   - a multi-stage Next.js `Dockerfile` (Next `output: 'standalone'` → a slim
//     runtime image with no build tooling) + its `.dockerignore`,
//   - `compose.prod.yaml` derived from the app's declared `infra` (postgres/redis,
