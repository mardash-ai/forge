export interface ProdComposeOptions {
  appName: string;
  port: number;
  host: string;
  readinessPath: string;
  webImage: string; // digest-pinned (R1)
  dataPlaneImage: string; // digest-pinned (R1)
  withPostgres: boolean;
  withRedis: boolean;
  secrets: string[];
  certResolver: string;
  // The app declares scheduled jobs (a `forge.jobs.json` at the repo root). When set,
  // the compose bind-mounts that file into the data-plane sidecar and pins
  // FORGE_JOBS_FILE at it, so C2 registers the jobs on boot (P7.3).
  withJobs?: boolean;
  // The app declares a theme (`forge.theme.json`, C16). When set, the compose mounts
  // it into the data-plane sidecar and pins FORGE_THEME_FILE, so the hosted auth +
  // status pages render branded in production.
  withTheme?: boolean;
  // P26 — platform storage on Postgres. When set, the sidecar gets FORGE_STORE_BACKEND=postgres +
  // FORGE_DB_URL pointing at a SEPARATE `forge_platform` database with its own least-privilege role
  // (co-located on the app's Postgres instance, or a dedicated one when the app has no DB of its own).
  // The sidecar depends on Postgres being healthy. See docs/architecture/08-storage-strategy.md §6.
  platformDb?: boolean;
  // P33 — the blob (C20) backend: 'filesystem' (default; bytes on the durable forge_state volume) or
  // 's3' (bytes in an S3-compatible object store + metadata in Postgres). DECOUPLED from platformDb, so a
  // single-node Postgres deploy keeps blobs on the volume unless the app explicitly opts into S3. When
  // 's3', the sidecar gets FORGE_BLOBS_BACKEND=s3 and the operator supplies FORGE_S3_* in .env.prod.
  blobsBackend?: 'filesystem' | 's3';
  // Reverse-proxy network the old replica is drained out of on a roll (matches C7).
  proxyNet?: string;
  // C36 — MCP observability. When true, the web + data-plane services join the shared external
  // `observability` network and get the OTLP→Langfuse export env (endpoint + empty-default LANGFUSE
  // keys). The sidecar traces the MCP transport; the app continues the trace. Off by default.
  observability?: boolean;
}

// P34 — the OPTIONAL auth-provider vars the data-plane (which hosts C10 `/auth/*`) reads. When the app
// uses hosted auth they are wired into the sidecar env as defined-but-empty (`${NAME:-}`) even if not
// separately declared, so an operator fills them in .env.prod alone (the single source of truth) with no
// re-provision. Absence stays detectable (`GET /auth/config` reports google:false), never a crash.
const AUTH_PROVIDER_VARS = ['GOOGLE_CLIENT_ID', 'GOOGLE_CLIENT_SECRET', 'SMTP_URL', 'EMAIL_FROM'] as const;
// The token the C2 scheduler presents on a cron fire and the app verifies. Its presence in the declared
// set marks an app "uses hosted auth" for the P34 wiring above.
const AUTH_SESSION_SECRET_VAR = 'AUTH_SESSION_SECRET';
const AUTH_SERVICE_TOKEN_VAR = 'AUTH_SERVICE_TOKEN';
// P36 — when the app declares scheduled jobs (`/api/cron/*`), the service token is DEPLOY-REQUIRED: the
// scheduler must present it and the app must verify it, else cron fires are bare unauthenticated POSTs to
// publicly-routed endpoints. Fail the deploy loudly (`${VAR:?…}`) when jobs are declared but it is unset.
const CRON_TOKEN_REQUIRED_REASON =
  'set AUTH_SERVICE_TOKEN in .env.prod — this app declares scheduled cron jobs (/api/cron/*); the C2 ' +
  'scheduler presents it on each fire and the app must verify it, else cron endpoints are publicly ' +
  'reachable and fire UNAUTHENTICATED (Traefik routes /api/* publicly)';

const DATA_PLANE_PORT = 3718;

// P26 — the platform datastore: a SEPARATE logical database + least-privilege role, NOT the app's DB.
// Isolation (a platform bug can't touch app tables and vice versa) + least privilege + independent
// migration ownership. Co-located on the app's Postgres instance by default (swap the URL to move it
// to a dedicated/managed instance later).
export const PLATFORM_DB_NAME = 'forge_platform';
export const PLATFORM_DB_ROLE = 'forge_platform';
// Generated init file mounted into the app's Postgres (`/docker-entrypoint-initdb.d/`) that creates the
// least-priv role + database on first init — only needed when the app ALSO has its own DB (role `forge`);
// when the app has no DB, the dedicated platform Postgres is initialized directly as this role/db by the
// image entrypoint (no file).
//
// P31 — this is a `.sql` file (run by the Postgres entrypoint via `psql -f`), NOT an executable `.sh`.
// A bind-mounted `.sh` in `/docker-entrypoint-initdb.d/` gets EXECUTED, and on Docker Desktop for Mac the
// mount is effectively noexec, so first boot dies with `bad interpreter: Permission denied` → the
// forge_platform role/db never get created → the data-plane crashes at `ensureIdentitySchema`. A `.sql`
// file is only READ by psql (no exec bit, no interpreter), so it works identically on Linux and macOS.
export const PLATFORM_DB_INIT_FILE = 'forge-platform-init.sql';

// The FORGE_DB_URL the sidecar dials — the platform role/db, password from .env.prod (never committed).
export function platformDbUrl(): string {
  return `postgres://${PLATFORM_DB_ROLE}:\${FORGE_PLATFORM_DB_PASSWORD:?set FORGE_PLATFORM_DB_PASSWORD in .env.prod}@postgres:5432/${PLATFORM_DB_NAME}`;
}

// First-init SQL: create the least-privilege platform role + its own database, idempotently. The password
// is read from the Postgres container env at init time via psql's backtick `\set` (kept out of the
// committed file) and safely quoted with `format(%L)`, so a special char in the password can't break the
// statement. The role is a plain LOGIN role that OWNS only `forge_platform` — no rights on the app's DB.
// P31 — a `.sql` file (psql `-f`), so it needs no exec bit and runs on Docker Desktop for Mac too.
export function generatePlatformDbInitSql(): string {
  return `-- Generated by Forge — Productionize (P26/P31). Creates the least-privilege platform datastore
-- role + database on first Postgres init. Runs ONLY on an empty data dir; for an existing volume, an
-- operator runs the equivalent SQL once. Do not hand-edit; re-run \`forge productionize\` instead.
\\set ON_ERROR_STOP on
-- Read the platform role password from the container env (injected via compose; never committed here).
\\set pw \`printf '%s' "$FORGE_PLATFORM_DB_PASSWORD"\`
-- Create the least-privilege LOGIN role, idempotently (%L safely quotes the password literal).
SELECT format('CREATE ROLE ${PLATFORM_DB_ROLE} LOGIN PASSWORD %L', :'pw')
  WHERE NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = '${PLATFORM_DB_ROLE}')\\gexec
-- Create its OWN database, owned by that role, idempotently.
SELECT 'CREATE DATABASE ${PLATFORM_DB_NAME} OWNER ${PLATFORM_DB_ROLE}'
  WHERE NOT EXISTS (SELECT 1 FROM pg_database WHERE datname = '${PLATFORM_DB_NAME}')\\gexec
`;
}

