export interface ProvisioningRunbookOptions {
  appName: string;
  host: string;
  withPostgres: boolean;
  secrets: string[];
  withJobs?: boolean;
}

// The exact commands to set one secret — the dev path (C5 vault, control plane) and
// the prod path (`.env.prod` + redeploy). Kept as a helper so every row is consistent.
function setCommandLines(appName: string, name: string): string[] {
  return [
    `- **Dev / control plane** (encrypted C5 vault): \`./forge secrets set --app ${appName} --name ${name} --from-env ${name}\``,
    `  (export \`${name}\` first, or use \`--value <v>\`; \`--from-env\` keeps it out of shell history).`,
    `- **Prod / deployed**: put \`${name}=<value>\` in \`app/.env.prod\`, then redeploy so it's injected: \`./forge deploy --app ${appName}\`.`,
  ];
}

// The subset of the matrix THIS app needs, in provisioning order: the C5 vault key,
// the DB password (if provisioned with a DB), then every declared secret.
function neededSecretNames(opts: ProvisioningRunbookOptions): string[] {
  const names = ['FORGE_SECRETS_KEY'];
  if (opts.withPostgres) names.push('POSTGRES_PASSWORD');
  for (const s of opts.secrets) if (!names.includes(s)) names.push(s);
  return names;
}

export function generateProvisioningRunbook(opts: ProvisioningRunbookOptions): string {
  const needed = neededSecretNames(opts);
  const declared = new Set(opts.secrets);
  const usesAuth = declared.has(AUTH_SESSION_SECRET_NAME);
  const hasGoogle = GOOGLE_SECRET_NAMES.every((n) => declared.has(n));
  const hasEmail = EMAIL_SECRET_NAMES.every((n) => declared.has(n));

  const out: string[] = [];
  out.push(`# Provisioning — ${opts.appName}`);
  out.push('');
  out.push('> Generated by Forge — Productionize. Do not hand-edit; re-run `forge productionize` instead.');
  out.push('> It lists exactly the secrets **this** app needs (a subset of the full matrix in `forge/PROVISIONING.md`).');
  out.push('');
  out.push(`Public host: \`${opts.host}\`. Real values live in \`app/.env.prod\` (never committed) — this file only explains them.`);
  out.push('');

  // Checklist table.
  out.push('## What this app needs');
  out.push('');
  out.push('| Secret | Capability | Requirement | What it is |');
  out.push('|---|---|---|---|');
  for (const name of needed) {
    const spec = describeSecret(name);
    out.push(`| \`${name}\` | ${spec.capability} | ${requirementLabel(spec)} | ${spec.what} |`);
  }
  out.push('');

  // Per-secret detail.
  out.push('## Set each value');
  out.push('');
  for (const name of needed) {
    const spec = describeSecret(name);
    out.push(`### \`${name}\` — ${requirementLabel(spec)}`);
    out.push('');
    out.push(`- **Capability:** ${spec.capability}`);
    out.push(`- **What:** ${spec.what}`);
    out.push(`- **When needed:** ${spec.requires_note}`);
    out.push(`- **Obtain:** ${spec.obtain}`);
    if (spec.generate) out.push(`- **Generate:** \`${spec.generate}\``);
    out.push(...setCommandLines(opts.appName, name));
    out.push('');
  }

  // The reported gap: with neither Google nor SMTP, there is NO working signup path.
  if (usesAuth) {
    out.push('## Enabling a working sign-in method (C10)');
    out.push('');
    out.push('This app uses hosted auth (C10). **A user needs at least one working sign-in method.** With *neither* Google OAuth *nor* SMTP configured, `/auth/signup` shows **no form** and reports that email sign-up is unavailable — there is **no way to sign in**. Configure at least one:');
    out.push('');
    out.push(`- **Google sign-in** — set \`GOOGLE_CLIENT_ID\` + \`GOOGLE_CLIENT_SECRET\`. Works with **no email dependency**. Authorized redirect URI: \`https://${opts.host}/auth/google/callback\`.${hasGoogle ? ' _(declared for this app)_' : ' _(not declared yet — see below)_'}`);
    out.push(`- **Email + password** — set \`SMTP_URL\` + \`EMAIL_FROM\`. Unlocks email/password **signup + verification + password reset**.${hasEmail ? ' _(declared for this app)_' : ' _(not declared yet — see below)_'}`);
    out.push('');
    if (!hasGoogle || !hasEmail) {
      const missing = [...(!hasGoogle ? GOOGLE_SECRET_NAMES : []), ...(!hasEmail ? EMAIL_SECRET_NAMES : [])];
      out.push('If a method above is **not declared** for this app, declare it first so the compose injects it, then set it and redeploy:');
      out.push('');
      out.push('```sh');
      out.push(`./forge provision --app ${opts.appName} ${missing.map((n) => `--secret ${n}`).join(' ')}`);
      out.push(`./forge productionize --app ${opts.appName}   # regenerates compose.prod.yaml + this runbook`);
      out.push(`# add the value(s) to app/.env.prod, then:`);
      out.push(`./forge deploy --app ${opts.appName}`);
      out.push('```');
      out.push('');
    }
  }

  if (opts.withJobs) {
    out.push('## Scheduled jobs (C2)');
    out.push('');
    out.push(`\`${JOBS_FILE}\` is mounted into the data-plane sidecar and registered on boot — no env needed.`);
    out.push('');
  }

  return out.join('\n');
}
