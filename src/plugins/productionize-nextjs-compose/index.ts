// ---------------------------------------------------------------------------
// next config — idempotently ensure `output: 'standalone'`
// ---------------------------------------------------------------------------

export interface StandalonePatchResult {
  content: string;
  changed: boolean;
  action: 'already-standalone' | 'injected' | 'unmanaged';
  warning?: string;
}

const STANDALONE_LINE = "  output: 'standalone',";

// A fresh Next config used when the app has no config file: standalone output (the
// slim image needs it) + the always-on C10 `/auth/*` data-plane rewrite (shared with
// the scaffold, so a from-scratch app matches a scaffolded one).
export function defaultNextConfig(): string {
  return forgeNextConfig({ standalone: true });
}

// Idempotently ensure `output: 'standalone'` in an existing Next config's TEXT.
// - Already declares any `output:` → left untouched (never clobber a hand value);
//   warns if it isn't 'standalone'.
// - Otherwise inject the line into the first recognizable config-object opener.
// - No recognizable object → return unmanaged + a warning (do not rewrite the file).
export function applyStandaloneOutput(text: string): StandalonePatchResult {
  const outputMatch = text.match(/output\s*:\s*['"]([^'"]+)['"]/);
  if (outputMatch) {
    const val = outputMatch[1];
    return {
      content: text,
      changed: false,
      action: 'already-standalone',
      ...(val === 'standalone'
        ? {}
        : {
            warning: `next config already sets output: '${val}' (not 'standalone') — left as-is; the standalone Dockerfile needs 'standalone'.`,
          }),
    };
  }
  const openers: RegExp[] = [
    /const\s+\w+\s*(?::\s*[^=]+)?=\s*\{/, // const nextConfig = {   /   const nextConfig: NextConfig = {
    /export\s+default\s*\{/, //             export default {
    /module\.exports\s*=\s*\{/, //          module.exports = {
    /defineConfig\s*\(\s*\{/, //            defineConfig({
  ];
  for (const re of openers) {
    const m = text.match(re);
    if (m && m.index !== undefined) {
      const insertAt = m.index + m[0].length;
      const content = text.slice(0, insertAt) + '\n' + STANDALONE_LINE + text.slice(insertAt);
      return { content, changed: true, action: 'injected' };
    }
  }
  return {
    content: text,
    changed: false,
    action: 'unmanaged',
    warning:
      "could not find a Next config object to set output: 'standalone' — add it manually so the standalone image builds.",
  };
}

